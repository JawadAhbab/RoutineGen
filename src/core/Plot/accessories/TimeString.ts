export const timeStringConverter = (timestr: TimeString) => {
  const [, hrstr, minstr, ampm] = timestr.match(/^(\d{2})(\d{2})(am|pm)$/)!
  const [hr, min, pm] = [+hrstr, +minstr, ampm === 'pm']
  const hrs = (hr === 12 ? 0 : hr) + (pm ? 12 : 0)
  return hrs * 60 + min
}

